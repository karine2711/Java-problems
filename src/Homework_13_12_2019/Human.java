package Homework_13_12_2019;

public class Human {
    public String firstname;
    public String lastname;
    public char gender;
    public String age;
    public float rating;

    public Human(String firstname, String lastname, char gender, String age, float rating) {
        this.firstname = firstname;
        this.lastname = lastname;
        this.gender = gender;
        this.age = age;
        this.rating = rating;
    }
}
